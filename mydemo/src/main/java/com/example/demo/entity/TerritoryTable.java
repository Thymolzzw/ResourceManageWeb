package com.example.demo.entity;

public class TerritoryTable {
    private int territoryID;
    private String territoryName;
    private String territoryIntroduction;

    public int getTerritoryID() {
        return territoryID;
    }

    public void setTerritoryID(int territoryID) {
        this.territoryID = territoryID;
    }

    public String getTerritoryName() {
        return territoryName;
    }

    public void setTerritoryName(String territoryName) {
        this.territoryName = territoryName;
    }

    public String getTerritoryIntroduction() {
        return territoryIntroduction;
    }

    public void setTerritoryIntroduction(String territoryIntroduction) {
        this.territoryIntroduction = territoryIntroduction;
    }
}
